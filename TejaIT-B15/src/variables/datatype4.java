package variables;

public class datatype4 {
	
	//number types default value is zero //
	byte b;
	short s;
	int i;
	long l;
	
	//number types default value is 0.0 //
	float f;
	double d;
	
	//number type default value is empty space //
	char c;
	
	//number type default value is false  //
	boolean bool;
	
	
public static void main(String[] args) {
	
	datatype4 dt4=new datatype4();
	
	System.out.println(dt4.b);
	System.out.println(dt4.s);
	System.out.println(dt4.i);
	System.out.println(dt4.l);
	System.out.println(dt4.f);
	System.out.println(dt4.d);
	System.out.println(dt4.c);
	System.out.println(dt4.bool);
	
	
	
}
}
